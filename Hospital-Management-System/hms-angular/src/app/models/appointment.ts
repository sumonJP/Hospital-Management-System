export class Appointment {
    p_id: any;
    p_name: any;
    dep_name: any;
    doc_name: any;
    c_fee: any;
    date: any;
    time: any;
    constructor(p_id: any, p_name: any, dep_name: any, doc_name: any, c_fee: any, date: any, time: any) {
        this.p_id = p_id;
        this.p_name = p_name;
        this.dep_name = dep_name;
        this.doc_name = doc_name;
        this.c_fee = c_fee;
        this.date = date;
        this.time = time;

    }
}

export class Patient {
    p_id: any
    name: any
    dob: any
    gender: any
    b_group: any
    email: any
    address: any
    mobile: any
    constructor(p_id: any, name: any, dob: any, gender: any, b_group: any, email: any, address: any, mobile: any) {
        this.p_id = p_id;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
        this.b_group = b_group;
        this.email = email;
        this.address = address;
        this.mobile = mobile;
    }
}
